package Desafio2;

public class Producao {
	int previsaoDemanda;
	int producaoNormal;
	int estoqueInicial;
	int estoqueFinal;
	
	
	
		
	public int getPrevisaoDemanda() {
		return previsaoDemanda;
	}




	public void setPrevisaoDemanda(int previsaoDemanda) {
		this.previsaoDemanda = previsaoDemanda;
	}




	public int getProducaoNormal() {
		return producaoNormal;
	}




	public void setProducaoNormal(int producaoNormal) {
		this.producaoNormal = producaoNormal;
	}




	public int getEstoqueInicial() {
		return estoqueInicial;
	}




	public void setEstoqueInicial(int estoqueInicial) {
		this.estoqueInicial = estoqueInicial;
	}




	public int getEstoqueFinal() {
		return estoqueFinal;
	}




	public void setEstoqueFinal(int estoqueFinal) {
		this.estoqueFinal = estoqueFinal;
	}




	
		
}
